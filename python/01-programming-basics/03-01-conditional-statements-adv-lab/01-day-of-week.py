#                       ** Day of the week **
#
#    Write a program that reads a number, entered by the user, and
# prints the day of the week. If the entered number is not in the range
# [1 - 7] - print "Error".
#
#    Input:
#       1. Number [1 - 7]
#
#    Output:
#       1. Day of the week (english), else: "Error"
#

# Input
num = int(input())

# Output
if 1 <= num <= 7:
    if num == 1:
        print('Minday')
    if num == 2:
        print('Tuesday')
    if num == 3:
        print('Wednesday')
    if num == 4:
        print('Thursday')
    if num == 5:
        print('Friday')
    if num == 6:
        print('Saturday')
    if num == 7:
        print('Sunday')
else:
    print("Error")
