#
#                         ** Working hours **
#
#    Write a program that reads the time in hours [0-24], and day of the
# week (text), and checks if the office is working. Working hours are
# from 10-18, Monday-Saturday.
#
#    Input:
#       1. Hour [0-24]
#       2. Day [Monday ... Sunday]
#
#    Output:
#       1. [open, closed]
#

# Input
h = int(input())        # Hour
d = input()             # Day

# Output
if 10 <= h <= 18 and d != "Sunday":
    print("open")
else:
    print("closed")
