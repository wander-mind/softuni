#
#                     ** Weekend or working day **
#
#    Write a program that reads a day of the week in text, entered by
# the user, and if the day is a working day - print "Working day", if
# a day off - "Weekend", and else - "Error".
#
#    Input:
#       1. Day [string]
#
#    Output:
#       1. "Working day"/"Weekend"/"Error"
#

# Input
day = input()

# Output
if day == "Monday" or day == "Tuesday" or day == "Wednesday" or day == "Thursday" or day == "Friday":
    print("Working day")
elif day == "Saturday" or day == "Sunday":
    print("Weekend")
else:
    print("Error")
