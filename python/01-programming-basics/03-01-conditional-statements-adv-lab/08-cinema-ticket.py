#
#                         ** Cinema ticket **
#
#    Write a program that reads the day of the week (text), entered by
# the user, prints the price of the cinema ticket, based on the day of
# the week.
#
#  __________________________________________________________
# / Monday|Tuesday|Wednesday|Thursday|Friday|Saturday|Sunday \
# |-------|-------|---------|--------|------|--------|-------|
# \  12   |  12   |   14    |   14   |  12  |   16   |  16   /
#  \--------------------------------------------------------/
#
#    Input:
#       1. Day [Monday ... Sunday]
#
#    Output:
#       1. Price [12, 14, 16], else "Invalid day"
#

# Input
day = input()

# Output
if day in ["Monday", 'Tuesday', 'Friday']:
    print(12)
elif day in ['Wednesday', 'Thursday']:
    print(14)
elif day in ['Saturday', 'Sunday']:
    print(16)
else:
    print("Invalid day")
