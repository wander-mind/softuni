#
#                        ** Summing seconds **
#
#    3 runners are racing and finish in some amount of seconds [1 - 50].
# Write a program that reads the time for the 3 runners, entered by the
# user, and calculates the sum in format "minutes:seconds". The seconds
# have to be printed in two digits (0:00).
#

# Input
tr1 = int(input())	# Time runner 1
tr2 = int(input())      # Time runner 2
tr3 = int(input())      # Time runner 3

# Calculation
tts = tr1 + tr2 + tr3	# Total time seconds

mins = tts // 60
secs = tts  % 60

# Output
print(f"{mins}:{secs:02}")
