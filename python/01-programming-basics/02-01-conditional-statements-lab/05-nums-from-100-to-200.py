#
#                         ** Numbers from **
#                          ** 100 to 200 **
#
#    Write a program that reads a whole number, entered by the user,
# and checks if it is under 100, in between 100 and 200, or over 200.
#
#    - if under 100		print "Less than 100"
#    - if between 100 & 200	print "Between 100 and 200"
#    - if over 200		print "Greater than 200"
#

# Input
num = int(input())

# Output
if num < 100:
    print("Less than 100")
elif 100 <= num <= 200:
    print("Between 100 and 200")
else:
    print("Greater than 200")
