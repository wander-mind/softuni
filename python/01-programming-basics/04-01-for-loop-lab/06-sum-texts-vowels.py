# ** Adding a string vowels **

#   /--------------------------\
#  / Letter | a | e | i | o | u \
# ||--------|-------------------||
#  \ Value  | 1 | 2 | 3 | 4 | 5 /
#   \--------------------------/

# Input
text = input()

# Calculation
vowel_values = {'a': 1, 'e': 2, 'i': 3, 'o': 4, 'u': 5}
sum = 0

for char in text:
    if char in vowel_values:
        sum += vowel_values[char]

# Output
print(sum)
